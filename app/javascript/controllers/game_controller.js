const canvas = document.getElementById("game_canvas");
const ctx = canvas.getContext("2d");

// Draw grid
function drawGrid() {
    horizontalSectors = 11;
    verticalSectors = 9;
    sectorW = canvas.width / horizontalSectors;
    sectorH = canvas.height / verticalSectors;

    // Draw vertical lines
    for (let i = 1; i < horizontalSectors; i++) {
        ctx.beginPath();
        ctx.moveTo(sectorW * i, 0);
        ctx.lineTo(sectorW * i, canvas.height);
        ctx.stroke();
    }

    // Draw horizontal lines
    for (let i = 1; i < verticalSectors; i++) {
        ctx.beginPath();
        ctx.moveTo(0, sectorH * i);
        ctx.lineTo(canvas.width, sectorH * i);
        ctx.stroke();
    }
}

drawGrid();